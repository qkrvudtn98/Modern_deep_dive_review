/** String.prototype.toUpperCase
 * toUpperCase 메서드는 대상 문자열을 모두 대문자로 변경한 문자열을 반환한다.
 */

const str = 'JongGiMan JaeHyun';
console.log(str.toUpperCase());

/** String.prototype.toLowerCase
 * toLowerCase 메서드는 대상 문자열을 모두 소문자로 변경한 문자열을 반환함
 */

const jh = 'JongGiMan JaeHyun';
console.log(jh.toLowerCase());

/** String.prototype.substring 메서드
 * substring() 메서드는 string 객체의 시작 인덱스로부터 종료 인덱스 전까지 문자열의 부분 문자열을 반환
 */

const jm = 'JjaJangMan';
console.log(jm.substring(2,4));